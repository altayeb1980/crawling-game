package com.tomtom.exception;

public class FormatMapException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public FormatMapException() {
		super();
	}

	public FormatMapException(String message) {
		super(message);
	}

	public FormatMapException(String message, Throwable cause) {
		super(message, cause);
	}

	public FormatMapException(Throwable cause) {
		super(cause);
	}

}
